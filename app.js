const PAGE_SIZE = 6;
let currentPage = 1;
let searchQuery = '';

function getFilteredProjects() {
  if (!searchQuery) return PROJECTS_STATIC;
  const q = searchQuery.toLowerCase();
  return PROJECTS_STATIC.filter(p => p.edr.toLowerCase().includes(q));
}

function renderProjects(lang) {
  const filtered = getFilteredProjects();
  const total = filtered.length;
  const totalPages = Math.ceil(total / PAGE_SIZE) || 1;
  currentPage = Math.min(currentPage, totalPages);

  const slice = filtered.slice((currentPage - 1) * PAGE_SIZE, currentPage * PAGE_SIZE);
  document.querySelector('#proj-tbl tbody').innerHTML = slice.map(p => {
    const statusLabel  = lang === 'en' ? (STATUS_EN[p.status] || p.status) : p.status;
    const nameLabel    = lang === 'en' ? (p.nameEN   || p.name)    : p.name;
    const correspLabel = lang === 'en' ? (p.correspEN || p.corresp) : p.corresp;
    return `<tr>
      <td>${p.date}</td>
      <td>${statusLabel}</td>
      <td class="mono">${p.dream}</td>
      <td class="small-txt">${correspLabel}</td>
      <td class="small-txt">${nameLabel}</td>
      <td class="mono">${p.edr}</td>
    </tr>`;
  }).join('');

  const pg = document.getElementById('pagination');
  let html = `<button class="pg-btn" id="pg-prev">&#8249;</button>`;
  const delta = 2;
  const pages = new Set([1, totalPages]);
  for (let i = Math.max(1, currentPage - delta); i <= Math.min(totalPages, currentPage + delta); i++) pages.add(i);
  let prev = 0;
  [...pages].sort((a,b) => a-b).forEach(i => {
    if (prev && i - prev > 1) html += `<span class="pg-dots">…</span>`;
    html += `<button class="pg-btn${i === currentPage ? ' active' : ''}" data-page="${i}">${i}</button>`;
    prev = i;
  });
  html += `<button class="pg-btn" id="pg-next">&#8250;</button>`;
  pg.innerHTML = html;

  pg.querySelectorAll('[data-page]').forEach(btn => {
    btn.addEventListener('click', () => {
      currentPage = +btn.dataset.page;
      renderProjects(currentLang);
    });
  });
  document.getElementById('pg-prev').addEventListener('click', () => {
    if (currentPage > 1) { currentPage--; renderProjects(currentLang); }
  });
  document.getElementById('pg-next').addEventListener('click', () => {
    if (currentPage < totalPages) { currentPage++; renderProjects(currentLang); }
  });
}

