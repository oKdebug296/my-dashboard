function updateDeepStateTooltips(lang) {
  if (!deepStateLayer) return;
  deepStateLayer.eachLayer(layer => {
    const raw = layer.feature?.properties?.name;
    if (!raw) return;
    const parts = raw.split(' /// ');
    const name = (lang === 'en' ? (parts[1] || parts[0]) : parts[0]).trim();
    layer.bindTooltip(name, { sticky: true, className: 'occ-tooltip' });
  });
}

