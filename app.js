function updateMapPopups(lang) {
  projectMarkers.forEach(m => {
    const p = m._projData;
    const name = lang === 'en' ? (p.nameEN || p.name) : p.name;
    const status = lang === 'en' ? (STATUS_EN[p.status] || p.status) : p.status;
    m.bindPopup(`<b>${name}</b><br>${p.date} · ${status}`);
  });
}

function applyMapFilters() {
  const active = {};
  document.querySelectorAll('.mfilters input[type=checkbox]').forEach(cb => {
    if (cb.checked) {
      const g = cb.dataset.group;
      if (!active[g]) active[g] = new Set();
      active[g].add(cb.dataset.val);
    }
  });
  const hasAny = Object.keys(active).length > 0;
  let visible = 0;
  projectMarkers.forEach(m => {
    const p = m._projData;
    const pass = hasAny && (
      (active.work   && active.work.has(p.work))    ||
      (active.sphere && active.sphere.has(p.sphere)) ||
      (active.obj    && active.obj.has(p.obj))       ||
      (active.status && active.status.has(p.status))
    );
    if (pass) { m.addTo(map); visible++; }
    else       { m.remove(); }
  });
  document.getElementById('obj-total').textContent = visible;
}


